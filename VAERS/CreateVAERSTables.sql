DROP TABLE IF EXISTS vax, symptoms, vaers;

CREATE TABLE vax (
	VAERS_ID INT,
	VAX_TYPE VARCHAR,
	VAX_MANU TEXT,
	VAX_LOT VARCHAR,
	VAX_DOSE_SERIES VARCHAR,
	VAX_ROUTE VARCHAR,
	VAX_SITE VARCHAR,
	VAX_NAME TEXT
);

CREATE TABLE symptoms (
	VAERS_ID INT,
	SYMPTOM1 TEXT,
	SYMPTOMVERSION1	VARCHAR,
	SYMPTOM2 TEXT,
	SYMPTOMVERSION2	VARCHAR,
	SYMPTOM3 TEXT,
	SYMPTOMVERSION3	VARCHAR,
	SYMPTOM4 TEXT,
	SYMPTOMVERSION4	VARCHAR,
	SYMPTOM5 TEXT,
	SYMPTOMVERSION5 VARCHAR
);

