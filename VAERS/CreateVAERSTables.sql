	DIED VARCHAR,
	DATEDIED VARCHAR,
	L_THREAT VARCHAR,
	ER_VISIT VARCHAR,
	HOSPITAL VARCHAR,
	HOSPDAYS VARCHAR,
	X_STAY VARCHAR,
	DISABLED VARCHAR,
	RECOVD VARCHAR,
	VAX_DATE VARCHAR,
	ONSET_DATE VARCHAR,
	NUMDAYS VARCHAR,
	LAB_DATA VARCHAR,
	V_ADMINBY VARCHAR,
	V_FUNDBY VARCHAR,
	OTHER_MEDS TEXT,
	CUR_ILL VARCHAR,
	HISTORY VARCHAR,
	PRIOR_VAX VARCHAR,
	SPLTTYPE VARCHAR,
	FORM_VERS VARCHAR,
	TODAYS_DATE VARCHAR,
	BIRTH_DEFECT VARCHAR,
	OFC_VISIT VARCHAR,
	ER_ED_VISIT VARCHAR,
	ALLERGIES TEXT
